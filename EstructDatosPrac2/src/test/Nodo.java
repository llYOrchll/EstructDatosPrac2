class Nodo {
    int valor;
    Nodo antLink;
    Nodo sigLink;

    public Nodo() {
        this.valor = valor;
        this.antLink = antLink;
        this.sigLink = sigLink;
    }

    public int getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }

    public Nodo getAntLink() {
        return antLink;
    }

    public void setAntLink(Nodo antLink) {
        this.antLink = antLink;
    }

    public Nodo getSigLink() {
        return sigLink;
    }

    public void setSigLink(Nodo sigLink) {
        this.sigLink = sigLink;
    }
    
    
}
